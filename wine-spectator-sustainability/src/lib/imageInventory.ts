// Utility functions for image management
export function getImagesByBrand(brand: ImageAsset['brand']): ImageAsset[] {
  return imageInventory.filter(img => img.brand === brand);
}

export function getImagesByLocation(location: NonNullable<ImageAsset['location']>): ImageAsset[] {
  return imageInventory.filter(img => img.location === location);
}

export function getImagesByCategory(category: ImageAsset['category']): ImageAsset[] {
  return imageInventory.filter(img => img.category === category);
}

export function getHeroImage(
  brand: ImageAsset['brand'],
  location?: NonNullable<ImageAsset['location']>
): ImageAsset | undefined {
  if (location) {
    return imageInventory.find(
      img => img.brand === brand && img.location === location && img.category === 'hero'
    );
  }

  return imageInventory.find(img => img.brand === brand && img.category === 'hero');
}

export function getGalleryImages(
  brand: ImageAsset['brand'],
  location?: NonNullable<ImageAsset['location']>
): ImageAsset[] {
  if (location) {
    return imageInventory.filter(
      img => img.brand === brand && img.location === location && img.category === 'gallery'
    );
  }

  return imageInventory.filter(img => img.brand === brand && img.category === 'gallery');
}

export function getLogos(): ImageAsset[] {
  return imageInventory.filter(img => img.category === 'logo');
}
